import '@testing-library/cypress/add-commands'

Cypress.Commands.add('searchFor', (handle) => {
  cy.findByPlaceholderText(/type an org's name/i).type(handle)
  cy.findByRole('button', {name: /search/i}).click()
  cy.location('pathname', {timeout: 50000}).should('include', handle)
})
